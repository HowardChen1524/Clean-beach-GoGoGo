package com.example.a1061524_1061525_final;

public class event {

    String name;
    String time;
    String location;

    public event(String name, String time, String location)
    {
        this.name = name;
        this.time = time;
        this.location = location;
    }

    public String getName() {
        return name;
    }
    public String getTime() {
        return time;
    }
    public String getLocation() {
        return location;
    }
}


